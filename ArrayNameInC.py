arr = [10, 20, 30]

# Print array reference (list in Python)
print(f"Array reference (arr): {arr}")

# Print values of arr[0] and arr[1]
print(f"Value of arr[0]: {arr[0]}")
print(f"Value of arr[1]: {arr[1]}")

# Print memory addresses of arr[0] and arr[1] using id()
print(f"Address of arr[0]: {id(arr[0])}")
print(f"Address of arr[1]: {id(arr[1])}")
